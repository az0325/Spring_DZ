package pack01;

public class Apple {
	String add;
	int age;

	public Apple(String add, int age) {
		this.add = add;
		this.age = age;
	}

	public String getAdd() {
		return add;
	}

	public void setAdd(String add) {
		this.add = add;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

}
